public class Operators {
    public static void main(String[] args) {
        int x = 12;
        int y = 20;

        System.out.println("Sum is : " + (x + y));
        System.out.println("Sub is : " + (x - y));
        System.out.println(x / y);
        System.out.println(x * y);
        System.out.println(x % y);

    }
}
